// ===== LANGUAGE ENGINE =====
let currentLang = localStorage.getItem('fi_lang') || 'pt';

function setLang(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  localStorage.setItem('fi_lang', lang);

  // RTL for Arabic
  const isRTL = lang === 'ar';
  document.documentElement.setAttribute('lang', lang);
  document.documentElement.setAttribute('dir', isRTL ? 'rtl' : 'ltr');

  const t = TRANSLATIONS[lang];

  // textContent elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // innerHTML elements (contain HTML tags like <br>, <em>)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  // placeholder attributes
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (t[key] !== undefined) el.setAttribute('placeholder', t[key]);
  });

  // service select options
  const select = document.getElementById('servicoSelect');
  if (select && t['cont.opts']) {
    const currentVal = select.value;
    select.innerHTML = t['cont.opts'].map((opt, i) =>
      `<option value="${i === 0 ? '' : opt}"${i === 0 ? '' : ''}>${opt}</option>`
    ).join('');
    select.value = currentVal;
  }

  // footer copyright (contains the year span)
  const copyEl = document.getElementById('footerCopy');
  if (copyEl && t['foot.copy']) {
    const year = new Date().getFullYear();
    copyEl.innerHTML = t['foot.copy'].replace('{year}', `<span id="year">${year}</span>`);
  }

  // active lang button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
}

// Apply saved language on load
setLang(currentLang);

